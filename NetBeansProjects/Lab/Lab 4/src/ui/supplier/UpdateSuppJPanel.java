    public void refreshTable() {
        DefaultTableModel model = (DefaultTableModel)this.tblSuppliers.getModel();
        model.setRowCount(0);
        Iterator<Supplier> ite = this.supplierDirectory.getSupplierList().iterator();

        while(ite.hasNext()) {
            Supplier s = ite.next();
            Object[] row = new Object[]{s.getSupplyName()};
            model.addRow(row);
        }

    }

