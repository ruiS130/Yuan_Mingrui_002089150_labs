    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        int row = this.tblSuppliers.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog((Component)null, "Please select a row from the table first", "Warning", 2);
        } else {
            Supplier s = (Supplier)this.tblSuppliers.getValueAt(row, 0);
            this.supplierDirectory.removeSupplier(s);
            this.refreshTable();
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int row = this.tblSuppliers.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog((Component)null, "Please select a row from the table first", "Warning", 2);
        } else {
            Supplier s = (Supplier)this.tblSuppliers.getValueAt(row, 0);
            ViewSupplier vs = new ViewSupplier(this.userProcessContainer, s);
            this.userProcessContainer.add("ViewSupplier", vs);
            CardLayout layout = (CardLayout)this.userProcessContainer.getLayout();
            layout.next(this.userProcessContainer);
        }
    }//GEN-LAST:event_btnViewActionPerformed

