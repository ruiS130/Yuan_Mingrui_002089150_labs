    private void btnEditSuppActionPerformed(ActionEvent evt) {
        int row = this.tblSuppliers.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog((Component)null, "Please select a row!", "Warning", 2);
        } else {
            Supplier selectedSupplier = (Supplier)this.tblSuppliers.getValueAt(row, 0);
            EditSuppJPanel esjp = new EditSuppJPanel(this.userProcessContainer, this.supplierDirectory, selectedSupplier);
            this.userProcessContainer.add("EditSuppJPanel", esjp);
            CardLayout layout = (CardLayout)this.userProcessContainer.getLayout();
            layout.next(this.userProcessContainer);
        }
    }

