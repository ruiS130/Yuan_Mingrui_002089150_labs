    private void btnRemoveFeatureActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveFeatureActionPerformed
        // TODO add your handling code here:
        this.saveFeatures();
        int selectedRow = this.tblFeatures.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row!", "Warning", 2);
        } else {
            this.product.getFeatures().remove(selectedRow);
            this.refreshTable();
        }
       
    }//GEN-LAST:event_btnRemoveFeatureActionPerformed

    private void btnAttachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAttachActionPerformed
        // TODO add your handling code here:
        int returnVal = this.fileChooser.showOpenDialog(this);
        if (returnVal == 0) {
            File file = this.fileChooser.getSelectedFile();

            try {
                URL url = file.toURI().toURL();
                this.logoImage = new ImageIcon(url);
                this.logoImage = new ImageIcon(this.logoImage.getImage().getScaledInstance(150, 150, 4));
                this.imgLogo.setIcon(this.logoImage);
            } catch (MalformedURLException var6) {
                MalformedURLException ex = var6;
//                Logger.getLogger(this.getName()).log(Level.SEVERE, (String)null, ex);
            }
        }

    }//GEN-LAST:event_btnAttachActionPerformed

