    public void refreshTable() {
        DefaultTableModel model = (DefaultTableModel)this.tblFeatures.getModel();
        model.setRowCount(0);
        Iterator var2 = this.product.getFeatures().iterator();

        while(var2.hasNext()) {
            Feature f = (Feature)var2.next();
            Object[] row = new Object[]{f, f.getValue() == null ? "Empty" : f.getValue().toString()};
            model.addRow(row);
        }

    }


