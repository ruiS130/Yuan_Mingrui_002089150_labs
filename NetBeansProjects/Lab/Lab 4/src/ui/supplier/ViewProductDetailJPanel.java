    private void backAction() {

        this.workArea.remove(this);
        Component[] componentArray = this.workArea.getComponents();
        Component component = componentArray[componentArray.length - 1];
        ManageProductCatalogJPanel manageProductCatalogJPanel = (ManageProductCatalogJPanel)component;
        manageProductCatalogJPanel.refreshTable();
        CardLayout layout = (CardLayout)this.workArea.getLayout();
        layout.previous(this.workArea);
    }
    
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        this.product.setPrice(Integer.parseInt(this.txtPrice.getText()));
        this.product.setName(this.txtName.getText());
        this.product.setLogoImage(this.logoImage);
        this.saveFeatures();
        this.txtName.setEditable(false);
        this.txtPrice.setEditable(false);
        this.btnSave.setEnabled(false);
        this.tblFeatures.setEnabled(false);
        this.btnAddFeature.setEnabled(false);
        this.btnAttach.setEnabled(false);
        this.btnRemove.setEnabled(false);
        JOptionPane.showMessageDialog(this, "Product information saved.", "Information", 1);
        this.backAction();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void saveFeatures() {
       DefaultTableModel model = (DefaultTableModel)this.tblFeatures.getModel();

        for(int i = 0; i < model.getRowCount(); ++i) {
            Feature currentFeature = (Feature)this.product.getFeatures().get(i);
            currentFeature.setName(this.tblFeatures.getValueAt(i, 0).toString());
            currentFeature.setValue(this.tblFeatures.getValueAt(i, 1));
        }
    }

