    public void refreshTable() {
        DefaultTableModel model = (DefaultTableModel)this.tblProducts.getModel();
        model.setRowCount(0);
        Iterator var2 = this.supplier.getProductCatalog().getProductCatalog().iterator();

        while(var2.hasNext()) {
            Product p = (Product)var2.next();
            Object[] row = new Object[]{p, p.getId(), p.getPrice()};
            model.addRow(row);
        }

        if (this.supplier.getLogoImage() != null) {
            ImageIcon logo = this.supplier.getLogoImage();
            ImageIcon logoImage = new ImageIcon(logo.getImage().getScaledInstance(150, 150, 4));
            this.imgLogo.setIcon(logoImage);
        } else {
            this.imgLogo.setText("");
        }

    }

