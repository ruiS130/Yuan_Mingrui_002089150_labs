    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = this.tblProducts.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog((Component)null, "Please select a row from the table first", "Warning", 2);
        } else {
            Product selectedProduct = (Product)this.tblProducts.getValueAt(selectedRow, 0);
            this.supplier.getProductCatalog().removeProduct(selectedProduct);
            JOptionPane.showMessageDialog((Component)null, "Product has been deleted.", "Success", 1);
            this.refreshTable();
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed
