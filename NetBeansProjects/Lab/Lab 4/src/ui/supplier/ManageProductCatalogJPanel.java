    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = this.tblProducts.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog((Component)null, "Please select a row from the table first", "Warning", 2);
        } else {
            Product product2 = (Product)this.tblProducts.getValueAt(selectedRow, 0);
            if (product2 != null) {
                ViewProductDetailJPanel vpdjp = new ViewProductDetailJPanel(this.workArea, product2);
                this.workArea.add("ViewProductDetailJPanel", vpdjp);
                CardLayout layout = (CardLayout)this.workArea.getLayout();
                layout.show(this.workArea, "ViewProductDetailJPanel");
            } else {
                JOptionPane.showMessageDialog((Component)null, "Product is Null", "Warning", 0);
            }

        }
    }//GEN-LAST:event_btnViewActionPerformed

